package com.study.shape;

public class ChangFangXing {

    public static void main(String[] args) {
        //打印长方形
        int [] a=new int[10];
        for (int i = 0; i < a.length; i++) {
            for (int j = 0; j < a.length; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
        System.out.println();
    }
}
